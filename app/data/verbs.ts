export interface PresentTenseConjugations {
  ja: string;
  ty: string;
  onOnaOno: string; // on/ona/ono/pan/pani
  my: string;
  wy: string;
  oniOne: string; // oni/one/państwo
}

export interface PastTenseConjugations {
  // Gender-specific for 1st and 2nd person singular
  jaMasc: string;
  jaFem: string;
  tyMasc: string;
  tyFem: string;
  // 3rd person singular (by gender)
  on: string;
  ona: string;
  ono: string;
  // Plural with masculine-personal vs other
  myMascPersonal: string;
  myOther: string;
  wyMascPersonal: string;
  wyOther: string;
  // 3rd person plural
  oni: string; // masculine-personal
  one: string; // non-masculine-personal
}

// Future tense for imperfective verbs uses "być" + l-participle (same forms as past tense)
// Format: będę/będziesz/będzie/będziemy/będziecie/będą + past participle
export interface FutureTenseConjugations {
  // Gender-specific for 1st and 2nd person singular
  jaMasc: string;
  jaFem: string;
  tyMasc: string;
  tyFem: string;
  // 3rd person singular (by gender)
  on: string;
  ona: string;
  ono: string;
  // Plural with masculine-personal vs other
  myMascPersonal: string;
  myOther: string;
  wyMascPersonal: string;
  wyOther: string;
  // 3rd person plural
  oni: string; // masculine-personal
  one: string; // non-masculine-personal
}

export interface VerbConjugation {
  infinitive: string;
  meaning: string;
  conjugations: PresentTenseConjugations;
  pastTense: PastTenseConjugations;
  futureTense: FutureTenseConjugations;
  group: string; // verb conjugation group/pattern
}

