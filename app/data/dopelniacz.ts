// Polish Dopełniacz (Genitive Case) Sentence Practice
// Fill in the blank with the correct genitive form
// Answers the questions: Kogo? Czego? (Whom? What? - of/from)

export type Gender =
  | "masculine_animate"
  | "masculine_inanimate"
  | "feminine"
  | "neuter";

export type DopelniaczExercise = {
  sentence: string; // Sentence with ___ for blank
  nominative: string; // Base form of the noun (shown to user)
  answer: string; // Correct genitive form
  gender: Gender;
  translation: string; // English translation
  category: string;
  hint?: string;
};

