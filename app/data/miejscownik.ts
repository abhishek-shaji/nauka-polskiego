// Helper function to get a random sentence
export function getRandomMiejscownikSentence(
  category?: string | null
): MiejscownikExercise {
  const filteredSentences = category
    ? miejscownikSentences.filter((s) => s.category === category)
    : miejscownikSentences;
  return filteredSentences[
    Math.floor(Math.random() * filteredSentences.length)
  ];
}

